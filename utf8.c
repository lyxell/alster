uint32_t decode(uint32_t* state, int byte) {
  uint32_t type = utf8d[byte];
  *state = utf8d[256 + *state*16 + type];
  return *state;
}

char *getchar_utf8() {
    static char buf[5];
    uint32_t state = 0;
    for (int i = 0; i < 4; i++) {
        buf[i] = getchar();
        if (decode(&state, buf[i]) == UTF8_ACCEPT) {
            buf[i+1] = '\0';
            return buf;
        }
    }
    buf[0] = '\0';
    return buf;
}

